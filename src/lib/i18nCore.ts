const resources = { en, zh } satisfies Record<Locale, Record<TranslationKey, string>>;

export type TranslationValues = Record<string, string | number>;

export function t(locale: Locale, key: TranslationKey | string, values?: TranslationValues) {
  const template =
    resources[locale][key as TranslationKey] ??
    resources.en[key as TranslationKey] ??
    key;

  return interpolate(template, values);
}

export function resolveLocale(
  languagePreference: LanguagePreference,
  languages = getSystemLanguages(),
): Locale {
  if (languagePreference === "en" || languagePreference === "zh") {
    return languagePreference;
  }

  const firstLanguage = languages.find(Boolean)?.toLowerCase() ?? "";
  return firstLanguage.startsWith("zh") ? "zh" : "en";
}

export function readLanguagePreference(): LanguagePreference {
  if (typeof window === "undefined") {
    return "system";
  }

  try {
    return normalizeLanguagePreference(
      window.localStorage.getItem(languagePreferenceStorageKey),
    );
  } catch {
    return "system";
  }
}

export function writeLanguagePreference(languagePreference: LanguagePreference) {
  if (typeof window === "undefined") {
    return;
  }

  try {
    window.localStorage.setItem(languagePreferenceStorageKey, languagePreference);
  } catch {
    // Language preference is a local convenience setting.
  }
}

function normalizeLanguagePreference(value: string | null): LanguagePreference {
  if (value === "en" || value === "zh" || value === "system") {
    return value;
  }

  return "system";
}

function getSystemLanguages() {
  if (typeof navigator === "undefined") {
    return ["en"];
  }

  return navigator.languages?.length
    ? Array.from(navigator.languages)
    : [navigator.language];
}

function interpolate(template: string, values?: TranslationValues) {
  if (!values) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}
